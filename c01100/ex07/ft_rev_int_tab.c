void	ft_rev_int_tab(int *tab, int size)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	if (!tab)
		return ;
	while (a < (size / 2))
	{
		b = tab[a];
		tab[a] = tab[size - 1 - a];
		tab[size - 1 - a] = b;
		a++;
	}
}
