int ft_sqrt(int nb)
{
	long	a;

	a = 1;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (0);
	while (a * a <= nb)
	{
		if (nb / a == a && nb % a == 0)
			return (a);
		a++;
	}
	return (0);
}

#include <stdio.h>

int	main()
{
	printf("%d", ft_sqrt(2147483647));
}
