int	ft_strcmp(char *s1, char *s2)
{
	int	a;

	a = 0;
	if (!s1 || !s2)
		return (0);
	while (s1[a] == s2[a] && s1[a] && s2[a])
		a++;
	return (s1[a] - s2[a]);
}
